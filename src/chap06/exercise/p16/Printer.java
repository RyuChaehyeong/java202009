package chap06.exercise.p16;

public class Printer {
	void println(int i) {
		System.out.println(i);
	}
	void println(boolean a) {
		System.out.println(a);
	}
	void println(double d) {
		System.out.println(d);
	}
	void println(String name) {
		System.out.println(name);
	}
	//system: class
	//out: field
	//println: method
}
